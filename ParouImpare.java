public class ParouImpare {
    public static void main(String[] args){

        int numero = 10;
        if (numero % 2 == 0){
            System.out.println("Esse numero eh par: "+ numero);
        }
        else if (numero % 2 != 0){
            System.out.println("Este numero eh impar : "+ numero);
        }


    }
}
